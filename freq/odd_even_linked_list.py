# Definition for singly-linked list.
# class ListNode(object):
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution(object):
    def oddEvenList(self, head):
        """
        :type head: ListNode
        :rtype: ListNode
        """
        dummy1 = odd = ListNode(-1)
        dummy2 = even = ListNode(-1)
        while head:
            odd.next = head
            odd = head
            even.next = odd.next
            even = even.next
            head = even.next if even else None
        odd.next = dummy2.next
        return dummy1.next


